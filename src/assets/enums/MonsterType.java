package assets.enums;

/**
 * The enumerator of the types of monsters available in this game.
 * Skeletons, spirits, trolls, zombies, and dragons are monsters that the player can purchase and add to their team.
 * Sand monster, Water monster, Mud monster, Snow monster and Grass monster are battle-specific monsters that belong to a battle location that the player's monsters will fight against.
 */
public enum MonsterType {
    SKELETON,
    SPIRIT,
    TROLL,
    ZOMBIE,
    DRAGON,

    SAND_MONSTER,
    WATER_MONSTER,
    MUD_MONSTER,
    SNOW_MONSTER,
    GRASS_MONSTER
}
